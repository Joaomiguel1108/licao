#Solicite ao usuário que digite dois números e imprima a soma, a diferença, o
#produto e o quociente deles.


numero1 = float(input("digite um numero"))
numero2 = float(input("digite outro numero"))

print(f"a soma de {numero1} e {numero2} é")
print(numero1+numero2)
print(f"a diferença de {numero1} e {numero2} é")
print(numero1/numero2)
print(f"o produto de {numero1} e {numero2} é")
print(numero1*numero2)
print(f"a quociente de {numero1} e {numero2} é")
print(numero1/numero2)
